              <h4>Déductibilités fiscale des stages sportifs</h4>
              <p>
                La garde des enfants peut poser de sérieux problèmes en particulier pendant les vacances scolaires lorsque les deux parents travaillent.
                Ainsi le gouvernement a décidé que les frais de particpations d'enfants de moins de 12 ans à un centre de vacance agréépeuvent faire l'objet d'une
                déduction fiscale. Voici les conditions d'obtention de cette déduction: 

                Les parents doivent bénéficier de revenus professionnels et avoir fiscalmenent à leur charge l'enfant qui participe au centre de vacances.
                L'enfant doit avoir moins de 12 ans au moment de sa participation au centre de vacances. 
                Le centre doit être agréé( Voir la liste des centres sur <a href="http://www.centres-de-vacances.be/ou/liste-cdv.php">centres de vacances</a> ) seul l'accueil "en dehors" des jours 
                et des heures de présence normales à l'école sera prise en compte. 
                Les clubs sportifs: la déduction des frais de garde pourrait également se faire pour des enfants inscrits dans un club sportif ayant reçu l'autorisation par 
                l'O.N.E.( Office Nationale de l'Enfance ) d'accueillir des enfants de moins de 6 ans. 

                Le montant de la déduction: le montant maximum des frais déductibles est fixé à 11,20€ par jour et par enfant.

                Remarque: ce montant s'entend par journée et par enfant ( ex: prix d'une demi-journée = journée complète déductible) mais ne comprend pas les frais de repas, de vêtements, ... 
                Si le stage est spécifique ( sportif, culturel ou autre ), la déductibilité est limitée à 50% et si les activités sont liées à la mission d'enseignement de l'école, 
                elles ne seront pas prises en compte.
              </p>
